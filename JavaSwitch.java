public class JavaSwitch {
    public static void main(String[] args) {
        int day = 1;
        switch (day) {
            case 1:
                System.out.println("Monday");
                break;
            case 2:
                System.out.println("Tuesday");
                break;
            case 3:
                System.out.println("Wednesday");
                break;
            case 4:
                System.out.println("Thursday");
                break;
            case 5:
                System.out.println("Friday");
                break;
            case 6:
                System.out.println("Saturday");
                break;
            case 7:
                System.out.println("Sunday");
                break;
            default:
                System.out.println("Enter a valid day number!");
        }

        // Q - Write a program in java to take 2 values and operator from user and print
        // the operation of two integers according to the given operator using switch
        // statement
        // Condition on operator
        // Hint: switch(operator)
    }
}
